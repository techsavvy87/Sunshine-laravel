<div class="space-y-4 text-sm text-base-content/70">
  <h4 class="text-xl font-semibold mb-4">Dashboard</h4>
  <p>The Dashboard provides a comprehensive overview of your business activities, appointments, revenue, and key metrics. It's your central command center for monitoring daily operations and business performance.</p>

  <div>
    <h5 class="font-semibold mb-2">Today's Revenue</h5>
    <p>The Dashboard displays your total revenue for today, giving you immediate insight into your daily earnings. This metric includes:</p>
    <ul class="list-disc list-inside space-y-1 ml-4">
      <li>All completed appointments with payments processed</li>
      <li>Revenue from services, packages, and additional charges</li>
      <li>Real-time updates as appointments are completed and invoices are paid</li>
      <li>Visual indicators showing performance compared to previous days</li>
    </ul>
    <p class="mt-2">This helps you track daily financial performance and identify trends in your business revenue.</p>
  </div>

  <div>
    <h5 class="font-semibold mb-2">Today's Appointments</h5>
    <p>The Dashboard shows all appointments scheduled for today, organized by status. You can see:</p>
    <ul class="list-disc list-inside space-y-1 ml-4">
      <li><strong>Pending Appointments:</strong> Newly created appointments awaiting confirmation</li>
      <li><strong>Confirmed Appointments:</strong> Appointments that have been confirmed and are ready</li>
      <li><strong>Scheduled:</strong> Appointments scheduled for today or coming up</li>
      <li><strong>In Progress / On Property:</strong> Services currently being performed; for boarding/daycare the pet is on property</li>
      <li><strong>Completed:</strong> Services that have been finished today</li>
      <li><strong>Cancelled:</strong> Appointments that were cancelled</li>
    </ul>
    <p class="mt-2">Each appointment card displays customer name, pet name, service type, scheduled time, and current status. Click on any appointment to view detailed information and manage it.</p>
  </div>

  <div>
    <h5 class="font-semibold mb-2">Customers and Pets Overview</h5>
    <p>The Dashboard provides quick access to customer and pet information:</p>
    <ul class="list-disc list-inside space-y-1 ml-4">
      <li><strong>Total Customers:</strong> View the total number of registered customers in your system</li>
      <li><strong>Total Pets:</strong> See the total number of pet profiles registered</li>
    </ul>
    <p class="mt-2">This information helps you understand your customer base and track growth in your business.</p>
  </div>

  <div>
    <h5 class="font-semibold mb-2">Revenue Statistics</h5>
    <p>The Dashboard includes comprehensive revenue statistics to help you analyze business performance:</p>
    <ul class="list-disc list-inside space-y-1 ml-4">
      <li><strong>Daily Revenue:</strong> Total revenue for the current day, comparison with previous days, breakdown by service type, and payment status tracking</li>
      <li><strong>Weekly Revenue:</strong> Total revenue for the current week, day-by-day breakdown, comparison with previous weeks, and trend analysis</li>
      <li><strong>Monthly Revenue:</strong> Total revenue for the current month, week-by-week progression, comparison with previous months, and growth percentage</li>
    </ul>
    <p class="mt-2">Revenue statistics are displayed in easy-to-read charts and graphs, allowing you to quickly identify trends, peak periods, and areas for improvement.</p>
  </div>

  <div>
    <h5 class="font-semibold mb-2">Recent Appointments</h5>
    <p>The Dashboard shows a list of recent appointments to help you stay updated on recent activity:</p>
    <ul class="list-disc list-inside space-y-1 ml-4">
      <li><strong>Chronological List:</strong> Appointments displayed in reverse chronological order (most recent first)</li>
      <li><strong>Quick Information:</strong> Each entry shows customer name, pet name, service, date, time, and status</li>
      <li><strong>Quick Actions:</strong> Access appointment details, edit, or manage directly from the list</li>
      <li><strong>Status Indicators:</strong> Color-coded status badges for quick visual identification</li>
      <li><strong>Filter Options:</strong> Filter recent appointments by status, service type, or date range</li>
    </ul>
    <p class="mt-2">Recent appointments help you track workflow, follow up on completed services, and ensure all appointments are properly processed.</p>
  </div>

