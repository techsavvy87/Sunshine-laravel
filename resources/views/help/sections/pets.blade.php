  <h4 class="text-xl font-semibold mb-4">Pets</h4>
  <p>Register and manage pet profiles including breed, color, coat type, vaccinations, certificates, questionnaires, and behavioral assessments.</p>

  <p class="mt-3 mb-2"><strong>Questionnaire:</strong></p>
  <p>Questionnaires are service category-specific forms that collect important information about pets before they receive services. Each service category (Grooming, Daycare, Training, etc.) has its own questionnaire.</p>
  <p class="mt-2"><strong>What Information is Collected:</strong></p>
  <ul class="list-disc list-inside space-y-1 ml-4">
    <li><strong>Grooming Questionnaire:</strong> Previous grooming history, social behavior with people and pets, crate training, physical issues, medications, and additional notes</li>
    <li><strong>Daycare Questionnaire:</strong> Social behavior, crate training, park visits, boarding history, daycare attendance history, and additional comments</li>
    <li><strong>Training Questionnaire:</strong> Social behavior, training goals, and specific training needs</li>
  </ul>
  <p class="mt-2"><strong>Questionnaire Status:</strong> Pending (submitted but not reviewed), Approved (reviewed and approved), or Rejected (reviewed and rejected, may require resubmission).</p>
  <p class="mt-2"><strong>How to Use:</strong> When editing a pet, click the "Questionnaire" button, select the appropriate service category tab, fill out all required fields, and save. Staff can review and change the questionnaire status.</p>

  <p class="mt-4 mb-2"><strong>Initial Temperament Assessment:</strong></p>
  <p>The Initial Temperament Assessment is a behavioral evaluation conducted when a pet first visits your facility. It helps staff understand how to safely handle and interact with the pet.</p>
  <p class="mt-2">The assessment evaluates:</p>
  <ul class="list-disc list-inside space-y-1 ml-4">
    <li><strong>Initial Greeting:</strong> How the pet responds when first meeting staff (Approachable, Shy, or Uncomfortable)</li>
    <li><strong>Touch Sensitivity:</strong> Assessment of how the pet reacts to being touched on different body areas (Body, Legs, Feet, Tail, Face, Nails)</li>
  </ul>
  <p class="mt-2">Each area is marked as either "Accepts" (comfortable with touch) or "Reacts" (shows discomfort or resistance). This assessment should be conducted by trained staff members who can safely evaluate the pet's behavior.</p>

  <p class="mt-4 mb-2"><strong>Grooming Note:</strong></p>
  <p>The Grooming Note displays the notes from the pet's most recent grooming appointment. This provides staff with important information about the pet's previous grooming experience.</p>
  <p class="mt-2">The note may include details about the pet's behavior, special handling requirements, preferences, or any issues encountered. It helps maintain continuity of care across multiple grooming visits.</p>
  <p class="mt-2 text-xs text-base-content/60"><strong>Note:</strong> The Grooming Note is automatically updated after each grooming appointment. It displays the most recent grooming notes only. To view older grooming notes, check the appointment history or archives.</p>

