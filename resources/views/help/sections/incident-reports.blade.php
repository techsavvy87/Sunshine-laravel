  <h4 class="text-xl font-semibold mb-4">Incident Reports</h4>
  <p>Create and manage incident reports for services. Document any incidents, accidents, or special occurrences during service delivery.</p>

  <p class="mt-3 mb-2"><strong>Features:</strong></p>
  <ul class="list-disc list-inside space-y-1 ml-4">
    <li>Create incident reports linked to appointments</li>
    <li>Document incident details and descriptions</li>
    <li>View and manage incident reports</li>
    <li>Edit or delete incident reports</li>
  </ul>

  <p class="mt-3 mb-2"><strong>How to Use:</strong></p>
  <p>Select a service, then create incident reports for specific appointments. Include detailed descriptions and any relevant information about the incident.</p>

  <p class="mt-3 mb-2"><strong>When Creating a Report:</strong></p>
  <ul class="list-disc list-inside space-y-1 ml-4">
    <li>Select the pets and staff involved</li>
    <li>Describe the incident in detail</li>
    <li>Upload photos if available</li>
    <li>Specify injury type (bite, laceration, tear, puncture, fracture, other) and location</li>
    <li>Indicate if treatment is needed and if it's an emergency</li>
    <li>Record whether the owner was contacted and conversation notes</li>
    <li>Document treatment type (in-house or vet), vet information, bills, and results</li>
    <li>Add a conclusion summarizing the incident and resolution</li>
  </ul>

  <p class="mt-3 mb-2"><strong>Appointment Status:</strong></p>
  <p>When an incident report is created, the related appointment(s) are automatically marked as "Issue" status. These appointments should be reviewed and resolved appropriately.</p>

