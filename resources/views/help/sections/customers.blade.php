  <h4 class="text-xl font-semibold mb-4">Customers</h4>
  <p>Manage customer information including contact details, addresses, and customer profiles. Customers are pet owners who use your services.</p>

  <p class="mt-3 mb-2"><strong>Additional Owners:</strong></p>
  <p>Some pets may have multiple owners (e.g., couples, family members). The system allows you to add additional owners to a customer profile:</p>
  <ul class="list-disc list-inside space-y-1 ml-4">
    <li>You can add up to 4 additional owners per customer</li>
    <li>Each additional owner requires a name and phone number</li>
    <li>Additional owners are useful for contact purposes and authorization</li>
    <li>All owners can be contacted regarding appointments and pet care</li>
  </ul>

  <p class="mt-4 mb-2"><strong>Cancellation/No Show History Table:</strong></p>
  <p>The system automatically tracks and displays a customer's cancellation and no-show history. This helps you identify patterns and manage customer relationships.</p>
  <p class="mt-2"><strong>What is Tracked:</strong></p>
  <ul class="list-disc list-inside space-y-1 ml-4">
    <li><strong>Cancellations:</strong> Appointments that were cancelled by the customer or staff</li>
    <li><strong>No Shows:</strong> Appointments where the customer didn't arrive and didn't cancel</li>
  </ul>
  <p class="mt-2">Each record includes the type (Cancelled or No Show), service name, date and time, and who cancelled it (staff member or system). The history is automatically updated when appointments are cancelled or marked as no-show.</p>

