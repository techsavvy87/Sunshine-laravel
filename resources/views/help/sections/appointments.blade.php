<div class="space-y-4 text-sm text-base-content/70">
  <h4 class="text-xl font-semibold mb-4">Appointments</h4>
  <p>Schedule and manage appointments for services. Track appointments through their lifecycle from pending to completed.</p>

  <p class="mt-3 mb-2"><strong>Appointment Statuses:</strong></p>
  <ul class="list-disc list-inside space-y-1 ml-4">
    <li><strong>Pending:</strong> Appointment created but not confirmed</li>
    <li><strong>Confirmed:</strong> Appointment confirmed and ready</li>
    <li><strong>Scheduled:</strong> Appointment is scheduled for today or coming up</li>
    <li><strong>In Progress / On Property:</strong> Service is being performed; for boarding/daycare the pet is on property</li>
    <li><strong>Completed:</strong> Service finished</li>
    <li><strong>Cancelled:</strong> Appointment cancelled</li>
  </ul>

  <p class="mt-3 mb-2"><strong>How to Create an Appointment:</strong></p>
  <ol class="list-decimal list-inside space-y-1 ml-4">
    <li>Click "New" on the appointments page</li>
    <li>Select a customer (or create new)</li>
    <li>Select a pet (or add new)</li>
    <li>Choose a service</li>
    <li>Select a date and available time slot</li>
    <li>Assign staff member (optional)</li>
    <li>Add notes (optional)</li>
    <li>Click "Create" to save</li>
  </ol>

  <p class="mt-3 mb-2"><strong>Package Appointments:</strong></p>
  <p>You can create appointments using customer packages. The system will track package usage and remaining credits.</p>

  <p class="mt-3 mb-2"><strong>Appointment Workflow:</strong></p>
  <ol class="list-decimal list-inside space-y-1 ml-4">
    <li>Create appointment (Pending)</li>
    <li>Confirm appointment (Confirmed)</li>
    <li>Check in customer and pet (Scheduled)</li>
    <li>Start service process (In Progress / On Property)</li>
    <li>Complete service and generate invoice (Completed)</li>
  </ol>

  <p class="mt-3 mb-2"><strong>Invoice Generation:</strong></p>
  <p>After completing a service, you can generate invoices with itemized charges, apply discounts, and track payments.</p>

